import requests
from bs4 import BeautifulSoup

url = "https://example.com"
response = requests.get(url)
soup = BeautifulSoup(response.content, "html.parser")

# Extract all the links from the webpage
links = soup.find_all("a")
for link in links:
    print(link.get("href"))
